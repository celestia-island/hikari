//! Traditional Chinese color definitions
//!
//! This module provides 161 traditional Chinese colors.
//!
//! Each color includes:
//! - Optional Chinese name (via `chinese-names` feature)
//! - RGB values (stored as u8 tuple)
//! - Color category
//! - Conversion methods: `hex()`, `rgba(alpha)`
//!
//! ## Features
//!
//! - `chinese-names`: Include Chinese color names in the binary.
//!   Disabled by default to minimize wasm binary size.
//!   Enable with: `hikari-palette = { features = ["chinese-names"] }`
//!
//! Data source: https://github.com/lanqy/chinese-colors

use serde::Deserialize;
#[allow(dead_code)]
use serde::Serialize;

mod chinese;
mod impl_;
#[cfg(test)]
mod tests;

pub use chinese::*;

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum ColorCategory {
    Red,
    Yellow,
    Green,
    Blue,
    Cyan,
    White,
    Black,
    Gray,
    Purple,
    Orange,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct Color {
    #[cfg(feature = "chinese-names")]
    pub name: &'static str,
    #[cfg(not(feature = "chinese-names"))]
    #[serde(skip)]
    pub name: (),
    pub rgb: (u8, u8, u8),
    pub category: ColorCategory,
}
